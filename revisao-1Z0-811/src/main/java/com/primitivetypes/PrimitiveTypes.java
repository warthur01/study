package com.primitivetypes;

public class PrimitiveTypes {
    public static void main(String[] args) {
        byte umByte = 100;              // Ocupa 1 byte (-128 a 127)
        short umShort = 32000;          // Ocupa 2 bytes
        int umInt = 100000;             // Ocupa 4 bytes
        long umLong = 10000000000L;     // Ocupa 8 bytes (note o 'L' no final)
    }
}
